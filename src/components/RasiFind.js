const returnDetail = function(birthName) {
    if (birthName.startsWith('A')) {
        birthName = birthName;
    } else if (birthName.startsWith('Th')) {
        birthName = birthName;
    } else if (birthName.startsWith('Sa')) {
        birthName = birthName;
    } else if (birthName.startsWith('D')) {
        birthName = birthName;
    } else if (birthName.startsWith('T')) {
        birthName = birthName;
    } else {
        birthName = birthName.toLowerCase();
    }

    let returnJSON = {};
    Object.keys(RasiJSON).forEach(function(val) {
        val.split(',').forEach(function(nameBegin) {
            if (birthName.startsWith(nameBegin)) {
                returnJSON = RasiJSON[val];
                returnJSON.name = birthName;
            }
        });
    });

    return returnJSON;
}

export default returnDetail;